package com.cmbchina.baas.easyBaas.request;

import com.cmbchina.baas.easyBaas.validation.PathValid;
import lombok.AllArgsConstructor;
import lombok.Data;
import lombok.NoArgsConstructor;
import org.springframework.validation.annotation.Validated;

import javax.validation.constraints.NotBlank;
import javax.validation.constraints.Pattern;

@Data
@AllArgsConstructor
@NoArgsConstructor
@Validated
public class BringNodeRequest {

    @NotBlank
    @Pattern(regexp = "^((2[0-4]\\d|25[0-5]|[01]?\\d\\d?)\\.){3}(2[0-4]\\d|25[0-5]|[01]?\\d\\d?)$")
    private String nodeAddress;

    private String nodeName;

    @PathValid(message = "路径格式不正确")
    private String filePath;

    private String username;

    private String password;
}
